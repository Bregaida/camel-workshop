package br.com.concretesolutions.workshops.camel.model;

public class PedidoRecusadoException extends Exception {

	public PedidoRecusadoException() {
	}

	public PedidoRecusadoException(String arg0) {
		super(arg0);
	}

	public PedidoRecusadoException(Throwable arg0) {
		super(arg0);
	}

	public PedidoRecusadoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
